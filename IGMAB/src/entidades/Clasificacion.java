public class Clasificacion {
  
	private int ClasificacionID;
    private String Descripcion;
    
    
    
    
    /**
  	 * @return the clasificacionID
  	 */
  	public int getClasificacionID() {
  		return ClasificacionID;
  	}
  	/**
  	 * @param p_clasificacionID the clasificacionID to set
  	 */
  	public void setClasificacionID(int p_clasificacionID) {
  		ClasificacionID = p_clasificacionID;
  	}
  	/**
  	 * @return the descripcion
  	 */
  	public String getDescripcion() {
  		return Descripcion;
  	}
  	/**
  	 * @param p_descripcion the descripcion to set
  	 */
  	public void setDescripcion(String p_descripcion) {
  		Descripcion = p_descripcion;
  	}
}
