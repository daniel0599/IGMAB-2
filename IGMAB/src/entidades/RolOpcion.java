package entidades;

public class RolOpcion {
	private int rolID;
	private int opcionID;
	private int rolOpcionID;
	
	public int getRolID() {
		return rolID;
	}
	public void setRolID(int rolID) {
		this.rolID = rolID;
	}
	public int getOpcionID() {
		return opcionID;
	}
	public void setOpcionID(int opcionID) {
		this.opcionID = opcionID;
	}
	public int getRolOpcionID() {
		return rolOpcionID;
	}
	public void setRolOpcionID(int rolOpcionID) {
		this.rolOpcionID = rolOpcionID;
	}
	
	
	
}

